#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new;
	int		len;

	if (!s1 && !s2)
		return (0);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	len = ft_strlen(s1) + ft_strlen(s2);
	new = (char *)malloc(sizeof(*new) * (len + 1));
	if (new == 0)
		return (0);
	ft_strcpy(new, s1);
	ft_strcat(new, s2);
	return (new);
}
